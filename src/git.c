char *hs_git_to_repo_path(const hs_git *g, const char *original) {
    char *expanded = hs_expand_path(original);
    if (!expanded) return strdup(original);

    size_t home_len = strlen(g->home_dir);
    if (strncmp(expanded, g->home_dir, home_len) == 0) {
        const char *rel = expanded + home_len;
        if (*rel == '/') rel++;
        char *result = strdup(rel);
        free(expanded);
        return result;
    }

    return expanded;
}

char *hs_git_to_original_path(const hs_git *g, const char *repo_relative) {
    return hs_join_path(g->home_dir, repo_relative);
}

void hs_conflict_file_free(hs_conflict_file *f) {
    if (!f) return;
    free(f->path);
    free(f->ours);
    free(f->theirs);
}

void hs_conflictvec_free(hs_conflictvec *v) {
    if (!v) return;
    for (size_t i = 0; i < v->len; i++)
        hs_conflict_file_free(&v->data[i]);
    hs_vec_free(v);
}
