static hs_exec_result git_exec(const hs_git *g, const hs_strvec *args) {
    hs_strvec full;
    hs_vec_init(&full);
    hs_vec_push(&full, strdup("git"));
    for (size_t i = 0; i < args->len; i++)
        hs_vec_push(&full, strdup(args->data[i]));
    hs_exec_result r = hs_exec_args_dir(&full, g->repo_path);
    hs_strvec_free(&full);
    return r;
}

static int mkdirs(const char *path) {
    if (!path || !path[0]) return -1;
    char *tmp = strdup(path);
    size_t len = strlen(tmp);
    if (len > 1 && tmp[len - 1] == '/') tmp[len - 1] = '\0';

    for (char *p = tmp + 1; *p; p++) {
        if (*p == '/') {
            *p = '\0';
            if (mkdir(tmp, 0755) != 0 && errno != EEXIST) {
                free(tmp);
                return -1;
            }
            *p = '/';
        }
    }
    int ret = (mkdir(tmp, 0755) != 0 && errno != EEXIST) ? -1 : 0;
    free(tmp);
    return ret;
}

static char *dir_of(const char *path) {
    char *dup = strdup(path);
    char *slash = strrchr(dup, '/');
    if (slash) {
        *slash = '\0';
    } else {
        free(dup);
        return strdup(".");
    }
    return dup;
}

static int copy_file(const char *src, const char *dst) {
    FILE *in = fopen(src, "rb");
    if (!in) return -1;
    FILE *out = fopen(dst, "wb");
    if (!out) { fclose(in); return -1; }

    char buf[8192];
    size_t n;
    int ok = 0;
    while ((n = fread(buf, 1, sizeof(buf), in)) > 0) {
        if (fwrite(buf, 1, n, out) != n) { ok = -1; break; }
    }
    if (ferror(in)) ok = -1;
    fclose(in);
    fclose(out);

    if (ok == 0) {
        struct stat st;
        if (stat(src, &st) == 0)
            chmod(dst, st.st_mode);
    }
    return ok;
}

static void copy_tree(const char *src, const char *dst);

static void copy_dir_recursive(const char *src, const char *dst) {
    mkdirs(dst);
    DIR *d = opendir(src);
    if (!d) return;
    struct dirent *ent;
    while ((ent = readdir(d)) != NULL) {
        if (strcmp(ent->d_name, ".") == 0 || strcmp(ent->d_name, "..") == 0)
            continue;
        char *s = hs_join_path(src, ent->d_name);
        char *dd = hs_join_path(dst, ent->d_name);
        copy_tree(s, dd);
        free(s);
        free(dd);
    }
    closedir(d);
}

static void copy_tree(const char *src, const char *dst) {
    struct stat st;
    if (stat(src, &st) != 0) return;

    if (S_ISDIR(st.st_mode)) {
        copy_dir_recursive(src, dst);
    } else if (S_ISREG(st.st_mode)) {
        char *parent = dir_of(dst);
        mkdirs(parent);
        free(parent);
        if (copy_file(src, dst) != 0)
            hs_error("failed to copy %s -> %s", src, dst);
    }
}

static char *relative_path(const char *path, const char *base) {
    size_t blen = strlen(base);
    if (strncmp(path, base, blen) != 0) return strdup(path);
    const char *rest = path + blen;
    if (*rest == '/') rest++;
    return strdup(rest);
}

static int str_compare(const void *a, const void *b) {
    return strcmp(*(const char **)a, *(const char **)b);
}

static void strvec_insert_sorted_unique(hs_strvec *v, const char *s) {
    for (size_t i = 0; i < v->len; i++) {
        int cmp = strcmp(v->data[i], s);
        if (cmp == 0) return;
    }
    hs_vec_push(v, strdup(s));
}

static void update_gitignore(const hs_git *g, const hs_groupvec *groups) {
    hs_strvec excludes;
    hs_vec_init(&excludes);

    strvec_insert_sorted_unique(&excludes, ".ssh/");
    strvec_insert_sorted_unique(&excludes, ".gnupg/");
    strvec_insert_sorted_unique(&excludes, "*secret*");
    strvec_insert_sorted_unique(&excludes, "*token*");
    strvec_insert_sorted_unique(&excludes, "*.key");
    strvec_insert_sorted_unique(&excludes, "*.pem");

    for (size_t i = 0; i < groups->len; i++) {
        const hs_sync_group *grp = &groups->data[i];
        for (size_t j = 0; j < grp->exclude.len; j++)
            strvec_insert_sorted_unique(&excludes, grp->exclude.data[j]);
    }

    qsort(excludes.data, excludes.len, sizeof(char *), str_compare);

    char *gitignore_path = hs_join_path(g->repo_path, ".gitignore");
    FILE *f = fopen(gitignore_path, "w");
    if (f) {
        fprintf(f, "# Generated by hyprsync\n");
        for (size_t i = 0; i < excludes.len; i++)
            fprintf(f, "%s\n", excludes.data[i]);
        fclose(f);
    }
    free(gitignore_path);
    hs_strvec_free(&excludes);
}

static void copy_to_repo(const hs_git *g, const char *source) {
    char *repo_rel = hs_git_to_repo_path(g, source);
    char *dest = hs_join_path(g->repo_path, repo_rel);

    if (hs_dir_exists(source)) {
        copy_dir_recursive(source, dest);
    } else if (hs_file_exists(source)) {
        char *parent = dir_of(dest);
        mkdirs(parent);
        free(parent);
        if (copy_file(source, dest) != 0)
            hs_error("failed to copy %s -> %s", source, dest);
    }

    free(repo_rel);
    free(dest);
}

static void copy_from_repo(const hs_git *g, const char *repo_file) {
    char *rel = relative_path(repo_file, g->repo_path);
    char *dest = hs_git_to_original_path(g, rel);

    if (hs_dir_exists(repo_file)) {
        copy_dir_recursive(repo_file, dest);
    } else if (hs_file_exists(repo_file)) {
        char *parent = dir_of(dest);
        mkdirs(parent);
        free(parent);
        if (copy_file(repo_file, dest) != 0)
            hs_error("failed to copy %s -> %s", repo_file, dest);
    }

    free(rel);
    free(dest);
}

hs_git *hs_git_create(const hs_git_config *config, const char *hostname) {
    hs_git *g = calloc(1, sizeof(hs_git));
    if (!g) return NULL;

    g->config.repo = hs_strdup_safe(config->repo);
    g->config.auto_commit = config->auto_commit;
    g->config.commit_template = hs_strdup_safe(config->commit_template);

    g->hostname = hs_strdup_safe(hostname);
    g->repo_path = hs_expand_path(config->repo);
    g->home_dir = hs_get_home_dir();

    return g;
}

void hs_git_free(hs_git *g) {
    if (!g) return;
    free(g->config.repo);
    free(g->config.commit_template);
    free(g->hostname);
    free(g->repo_path);
    free(g->home_dir);
    free(g);
}

int hs_git_init_repo(hs_git *g) {
    if (hs_git_is_initialized(g)) {
        hs_debug("git repo already initialized at %s", g->repo_path);
        return 1;
    }

    mkdirs(g->repo_path);
    if (!hs_dir_exists(g->repo_path)) {
        hs_error("failed to create repo directory: %s", g->repo_path);
        return 0;
    }

    chmod(g->repo_path, 0700);

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("init"));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    if (!hs_exec_success(&result)) {
        hs_error("git init failed: %s", result.stderr_output);
        hs_exec_result_free(&result);
        return 0;
    }
    hs_exec_result_free(&result);

    hs_vec_init(&args);
    hs_vec_push(&args, strdup("config"));
    hs_vec_push(&args, strdup("user.name"));
    hs_vec_push(&args, strdup("hyprsync"));
    result = git_exec(g, &args);
    hs_strvec_free(&args);
    if (!hs_exec_success(&result))
        hs_warn("failed to set git user.name");
    hs_exec_result_free(&result);

    hs_vec_init(&args);
    hs_vec_push(&args, strdup("config"));
    hs_vec_push(&args, strdup("user.email"));
    hs_vec_push(&args, strdup("hyprsync@localhost"));
    result = git_exec(g, &args);
    hs_strvec_free(&args);
    if (!hs_exec_success(&result))
        hs_warn("failed to set git user.email");
    hs_exec_result_free(&result);

    hs_info("initialized git repo at %s", g->repo_path);
    return 1;
}

int hs_git_is_initialized(const hs_git *g) {
    char *git_dir = hs_join_path(g->repo_path, ".git");
    int exists = hs_dir_exists(git_dir);
    free(git_dir);
    return exists;
}

void hs_git_snapshot(hs_git *g, const hs_groupvec *groups) {
    update_gitignore(g, groups);

    for (size_t i = 0; i < groups->len; i++) {
        const hs_sync_group *grp = &groups->data[i];
        for (size_t j = 0; j < grp->paths.len; j++) {
            char *expanded = hs_expand_path(grp->paths.data[j]);
            if (hs_file_exists(expanded) || hs_dir_exists(expanded)) {
                copy_to_repo(g, expanded);
            } else {
                hs_debug("skipping non-existent path: %s", expanded);
            }
            free(expanded);
        }
    }

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("add"));
    hs_vec_push(&args, strdup("-A"));
    hs_exec_result r = git_exec(g, &args);
    hs_strvec_free(&args);
    hs_exec_result_free(&r);
}

void hs_git_snapshot_changed(hs_git *g, const hs_strvec *changed_paths,
                             const hs_groupvec *groups) {
    update_gitignore(g, groups);

    for (size_t c = 0; c < changed_paths->len; c++) {
        const char *changed = changed_paths->data[c];
        int tracked = 0;

        for (size_t i = 0; i < groups->len && !tracked; i++) {
            const hs_sync_group *grp = &groups->data[i];
            for (size_t j = 0; j < grp->paths.len; j++) {
                char *expanded = hs_expand_path(grp->paths.data[j]);
                if (strncmp(changed, expanded, strlen(expanded)) == 0) {
                    tracked = 1;
                    free(expanded);
                    break;
                }
                free(expanded);
            }
        }

        if (tracked && (hs_file_exists(changed) || hs_dir_exists(changed)))
            copy_to_repo(g, changed);
    }

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("add"));
    hs_vec_push(&args, strdup("-A"));
    hs_exec_result r = git_exec(g, &args);
    hs_strvec_free(&args);
    hs_exec_result_free(&r);
}

void hs_git_restore(hs_git *g, const hs_groupvec *groups) {
    for (size_t i = 0; i < groups->len; i++) {
        const hs_sync_group *grp = &groups->data[i];
        for (size_t j = 0; j < grp->paths.len; j++) {
            char *expanded = hs_expand_path(grp->paths.data[j]);
            char *repo_rel = hs_git_to_repo_path(g, expanded);
            char *repo_file = hs_join_path(g->repo_path, repo_rel);

            if (hs_file_exists(repo_file) || hs_dir_exists(repo_file))
                copy_from_repo(g, repo_file);

            free(expanded);
            free(repo_rel);
            free(repo_file);
        }
    }
}

int hs_git_commit(hs_git *g, const char *message) {
    if (!hs_git_has_changes(g)) {
        hs_debug("no changes to commit");
        return 1;
    }

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("commit"));
    hs_vec_push(&args, strdup("-m"));
    hs_vec_push(&args, strdup(message));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    if (!hs_exec_success(&result)) {
        hs_error("commit failed: %s", result.stderr_output);
        hs_exec_result_free(&result);
        return 0;
    }

    hs_exec_result_free(&result);
    hs_info("committed: %s", message);
    return 1;
}

int hs_git_has_changes(const hs_git *g) {
    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("status"));
    hs_vec_push(&args, strdup("--porcelain"));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    int has = 0;
    if (hs_exec_success(&result)) {
        char *trimmed = hs_trim(result.stdout_output);
        has = (trimmed && trimmed[0] != '\0');
        free(trimmed);
    }
    hs_exec_result_free(&result);
    return has;
}

