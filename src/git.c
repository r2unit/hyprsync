hs_strvec hs_git_log(const hs_git *g, int count) {
    hs_strvec entries;
    hs_vec_init(&entries);

    char countbuf[32];
    snprintf(countbuf, sizeof(countbuf), "%d", count);

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("log"));
    hs_vec_push(&args, strdup("--oneline"));
    hs_vec_push(&args, strdup("-n"));
    hs_vec_push(&args, strdup(countbuf));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    if (hs_exec_success(&result)) {
        char *text = result.stdout_output;
        char *saveptr = NULL;
        char *line = strtok_r(text, "\n", &saveptr);
        while (line) {
            if (line[0] != '\0')
                hs_vec_push(&entries, strdup(line));
            line = strtok_r(NULL, "\n", &saveptr);
        }
    }
    hs_exec_result_free(&result);
    return entries;
}

hs_strvec hs_git_changed_files(const hs_git *g) {
    hs_strvec files;
    hs_vec_init(&files);

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("status"));
    hs_vec_push(&args, strdup("--porcelain"));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    if (hs_exec_success(&result)) {
        char *text = result.stdout_output;
        char *saveptr = NULL;
        char *line = strtok_r(text, "\n", &saveptr);
        while (line) {
            if (strlen(line) > 3)
                hs_vec_push(&files, strdup(line + 3));
            line = strtok_r(NULL, "\n", &saveptr);
        }
    }
    hs_exec_result_free(&result);
    return files;
}

hs_conflictvec hs_git_get_conflicts(const hs_git *g) {
    hs_conflictvec conflicts;
    hs_vec_init(&conflicts);

    hs_strvec args;
    hs_vec_init(&args);
    hs_vec_push(&args, strdup("diff"));
    hs_vec_push(&args, strdup("--name-only"));
    hs_vec_push(&args, strdup("--diff-filter=U"));
    hs_exec_result result = git_exec(g, &args);
    hs_strvec_free(&args);

    if (hs_exec_success(&result)) {
        char *text = result.stdout_output;
        char *saveptr = NULL;
        char *line = strtok_r(text, "\n", &saveptr);
        while (line) {
            if (line[0] != '\0') {
                hs_conflict_file cf;
                cf.path = strdup(line);
                cf.ours = NULL;
                cf.theirs = NULL;
                hs_vec_push(&conflicts, cf);
            }
            line = strtok_r(NULL, "\n", &saveptr);
        }
    }
    hs_exec_result_free(&result);
    return conflicts;
}

static time_t file_mtime(const char *path) {
    struct stat st;
    if (stat(path, &st) != 0) return 0;
    return st.st_mtime;
}

int hs_git_resolve_conflict(hs_git *g, const char *file,
                            hs_conflict_strategy strategy) {
    hs_exec_result result;
    memset(&result, 0, sizeof(result));

    hs_strvec args;

    switch (strategy) {
    case HS_CONFLICT_NEWEST_WINS: {
        char *repo_file = hs_join_path(g->repo_path, file);
        char *original = hs_git_to_original_path(g, file);

        if (hs_file_exists(repo_file) && hs_file_exists(original)) {
            time_t repo_time = file_mtime(repo_file);
            time_t orig_time = file_mtime(original);

            hs_vec_init(&args);
            hs_vec_push(&args, strdup("checkout"));
            if (orig_time > repo_time)
                hs_vec_push(&args, strdup("--ours"));
            else
                hs_vec_push(&args, strdup("--theirs"));
            hs_vec_push(&args, strdup(file));
            result = git_exec(g, &args);
            hs_strvec_free(&args);
        } else {
            hs_vec_init(&args);
            hs_vec_push(&args, strdup("checkout"));
            hs_vec_push(&args, strdup("--theirs"));
            hs_vec_push(&args, strdup(file));
            result = git_exec(g, &args);
            hs_strvec_free(&args);
        }

        free(repo_file);
        free(original);
        break;
    }

    case HS_CONFLICT_KEEP_BOTH: {
        char *repo_file = hs_join_path(g->repo_path, file);

        size_t ours_len = strlen(repo_file) + 1 + strlen(g->hostname) + 1;
        char *ours_file = malloc(ours_len);
        snprintf(ours_file, ours_len, "%s.%s", repo_file, g->hostname);

        size_t theirs_len = strlen(repo_file) + strlen(".remote") + 1;
        char *theirs_file = malloc(theirs_len);
        snprintf(theirs_file, theirs_len, "%s.remote", repo_file);

        size_t show_arg_len = 3 + strlen(file) + 1;
        char *show_ours_arg = malloc(show_arg_len);
        snprintf(show_ours_arg, show_arg_len, ":2:%s", file);

        hs_vec_init(&args);
        hs_vec_push(&args, strdup("show"));
        hs_vec_push(&args, show_ours_arg);
        hs_exec_result ours_result = git_exec(g, &args);
        hs_strvec_free(&args);

        if (hs_exec_success(&ours_result)) {
            FILE *f = fopen(ours_file, "w");
            if (f) {
                fputs(ours_result.stdout_output, f);
                fclose(f);
                hs_info("saved local version as: %s", ours_file);
            }
        }
        hs_exec_result_free(&ours_result);

        char *show_theirs_arg = malloc(show_arg_len);
        snprintf(show_theirs_arg, show_arg_len, ":3:%s", file);

        hs_vec_init(&args);
        hs_vec_push(&args, strdup("show"));
        hs_vec_push(&args, show_theirs_arg);
        hs_exec_result theirs_result = git_exec(g, &args);
        hs_strvec_free(&args);

        if (hs_exec_success(&theirs_result)) {
            FILE *f = fopen(theirs_file, "w");
            if (f) {
                fputs(theirs_result.stdout_output, f);
                fclose(f);
                hs_info("saved remote version as: %s", theirs_file);
            }
        }
        hs_exec_result_free(&theirs_result);

        hs_vec_init(&args);
        hs_vec_push(&args, strdup("checkout"));
        hs_vec_push(&args, strdup("--theirs"));
        hs_vec_push(&args, strdup(file));
        result = git_exec(g, &args);
        hs_strvec_free(&args);

        free(repo_file);
        free(ours_file);
        free(theirs_file);
        break;
    }

    case HS_CONFLICT_MANUAL:
    default:
        hs_info("manual resolution required for: %s", file);
        return 0;
    }

    if (hs_exec_success(&result)) {
        hs_exec_result_free(&result);
        hs_vec_init(&args);
        hs_vec_push(&args, strdup("add"));
        hs_vec_push(&args, strdup(file));
        hs_exec_result add_result = git_exec(g, &args);
        hs_strvec_free(&args);
        hs_exec_result_free(&add_result);
        return 1;
    }

    hs_exec_result_free(&result);
    return 0;
}

int hs_git_has_conflicts(const hs_git *g) {
    hs_conflictvec conflicts = hs_git_get_conflicts(g);
    int has = (conflicts.len > 0);
    hs_conflictvec_free(&conflicts);
    return has;
}

